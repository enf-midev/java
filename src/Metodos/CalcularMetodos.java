package Metodos;

import java.util.Scanner;

public class CalcularMetodos {
		int somar (int a, int b) {
			return a + b; 
			
		}
		int somar (int a, int b, int c) {
			return a + b + c;
		}
		int sub (int a, int b) {
			return a - b; 
		}
		int mul (int a, int b) {
			return a * b; 
		}
		int div (int a, int b) {
			return a / b; 
		}
}
